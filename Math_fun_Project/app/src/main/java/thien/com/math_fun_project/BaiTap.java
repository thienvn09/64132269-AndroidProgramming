package thien.com.math_fun_project;

public class BaiTap {
    private String img_BaiHoc;
    private String title_BaiHoc;
    public BaiTap(String img_BaiHoc, String title_BaiHoc) {
        this.img_BaiHoc = img_BaiHoc;
        this.title_BaiHoc = title_BaiHoc;
    }
    public String getImg_BaiHoc() {
        return img_BaiHoc;
    }
    public String getTitle_BaiHoc() {
        return title_BaiHoc;
    }
}
